/*
 * fitsBits - return 1 if x can be represented as an
 *  n-bit, two's complement integer.
 *   1 <= n <= 32
 *   Examples: fitsBits(5,3) = 0, fitsBits(-4,3) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 2
 */
int fitsBits(int x, int n) {
/* Since the positioning needs to be "32-n", the notation we will use is 33+~n since "-" operator is not allowed. We will shift the required bits to the left and make it the leading bit to check if number is positive or negative, and then shift them back to right by the same position.*/
	int y=x<<(33+~n);
	y=y>>(33+~n);

return !(y^x);
}
